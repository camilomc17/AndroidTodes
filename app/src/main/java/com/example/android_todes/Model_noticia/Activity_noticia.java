package com.example.android_todes.Model_noticia;

public class Activity_noticia {
    private int id_noticia;
    private String nombre_noticia;
    private String responsable_noticia;
    private String descripcion_noticia;

    public int getId_noticia() {
        return id_noticia;
    }

    public void setId_noticia(int id_noticia) {
        this.id_noticia = id_noticia;
    }



    public String getNombre_noticia() {
        return nombre_noticia;
    }

    public void setNombre_noticia(String nombre_noticia) {
        this.nombre_noticia = nombre_noticia;
    }



    public String getResponsable_noticia() {
        return responsable_noticia;
    }

    public void setResponsable_noticia(String responsable_noticia) {
        this.responsable_noticia = responsable_noticia;
    }

    public String getDescripcion_noticia() {
        return descripcion_noticia;
    }

    public void setDescripcion_noticia(String descripcion_noticia) {
        this.descripcion_noticia = descripcion_noticia;
    }



}
